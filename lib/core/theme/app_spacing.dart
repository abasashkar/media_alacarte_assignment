/// Spacing and radius design tokens used across the app for a consistent
/// rhythm. All values follow an 8-point system (with 4pt half-steps).
class AppSpacing {
  const AppSpacing._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 24;
  static const double xxl = 32;

  /// Vertical gap between major sections on a screen.
  static const double section = 28;

  /// Default inner padding for content cards.
  static const double cardPadding = 20;

  static const double radiusSm = 8;
  static const double radiusMd = 12;
  static const double radiusLg = 16;
  static const double radiusXl = 22;
  static const double radiusPill = 999;
}
